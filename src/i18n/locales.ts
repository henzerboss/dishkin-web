export function isSupportedLocale(locale: string): locale is Locale {
  return SUPPORTED_LOCALES.includes(locale as Locale);
}

export function isRtl(locale: string): boolean {
  return LANGUAGES.some((l) => l.code === locale && l.rtl);
}

export function t(locale: string, key: keyof typeof MESSAGES.en): string {
  const lang = isSupportedLocale(locale) ? locale : DEFAULT_LOCALE;
  return MESSAGES[lang][key] || MESSAGES.en[key] || String(key);
}

export function interpolate(template: string, values: Record<string, string | number>): string {
  return Object.entries(values).reduce((acc, [key, value]) => acc.replace(new RegExp(`{{${key}}}`, 'g'), String(value)), template);
}
